const { default: makeWASocket, DisconnectReason, jidDecode, proto, getContentType, useMultiFileAuthState, downloadContentFromMessage } = require("@whiskeysockets/baileys");
const pino = require('pino');
const { Boom } = require('@hapi/boom');
const fs = require('fs');
const readline = require("readline");
const PhoneNumber = require('awesome-phonenumber');

// Simple in-memory store for contacts and messages
const store = {
  contacts: {},
  messages: {},
  bind: (ev) => {
    ev.on('contacts.update', (updates) => {
      for (const update of updates) {
        if (update.id) store.contacts[update.id] = { ...store.contacts[update.id], ...update };
      }
    });
    ev.on('messages.upsert', (update) => {
      for (const msg of update.messages) {
        if (msg.key && msg.key.id) {
          store.messages[msg.key.id] = msg;
        }
      }
    });
  },
  loadMessage: async (chatId, messageId) => {
    return store.messages[messageId] || null;
  }
};

const question = (text) => {
  const rl = readline.createInterface({ input: process.stdin, output: process.stdout });
  return new Promise((resolve) => { rl.question(text, (answer) => { rl.close(); resolve(answer); }) });
};

//~~~~~Membuat Koneksi~~~~~//
async function StartHikariArchive() {
  const { state, saveCreds } = await useMultiFileAuthState("session");
  const Hikari = makeWASocket({
    logger: pino({ level: "silent" }),
    printQRInTerminal: false,
    auth: state,
    connectTimeoutMs: 60000,
    defaultQueryTimeoutMs: 0,
    keepAliveIntervalMs: 10000,
    emitOwnEvents: true,
    fireInitQueries: true,
    generateHighQualityLinkPreview: true,
    syncFullHistory: true,
    markOnlineOnConnect: true,
    browser: ["Ubuntu", "Chrome", "20.0.04"],
  });

  //~~~~~Meminta Kode~~~~~//
  if (!Hikari.authState.creds.registered) {
    const phoneNumber = await question('Masukan Nomor Bot :\n');
    let code = await Hikari.requestPairingCode(phoneNumber);
    code = code?.match(/.{1,4}/g)?.join("-") || code;
    console.log(`Pairing Kode kamu:`, code);
  }

  store.bind(Hikari.ev);

  Hikari.ev.on('messages.upsert', async chatUpdate => {
    try {
      const mek = chatUpdate.messages[0];
      if (!mek.message) return;
      mek.message = (Object.keys(mek.message)[0] === 'ephemeralMessage') ? mek.message.ephemeralMessage.message : mek.message;
      if (mek.key && mek.key.remoteJid === 'status@broadcast') return;
      if (!Hikari.public && !mek.key.fromMe && chatUpdate.type === 'notify') return;
      if (mek.key.id.startsWith('BAE5') && mek.key.id.length === 16) return;
      const m = smsg(Hikari, mek, store);
      require("./case")(Hikari, m, chatUpdate, store);
    } catch (err) {
      console.log(err);
    }
  });

  Hikari.decodeJid = (jid) => {
    if (!jid) return jid;
    if (/:\d+@/gi.test(jid)) {
      const decode = jidDecode(jid) || {};
      return decode.user && decode.server ? decode.user + '@' + decode.server : jid;
    }
    return jid;
  };

  Hikari.getName = (jid, withoutContact = false) => {
    const id = Hikari.decodeJid(jid);
    withoutContact = Hikari.withoutContact || withoutContact;
    if (id.endsWith("@g.us")) {
      return new Promise(async (resolve) => {
        let v = store.contacts[id] || {};
        if (!(v.name || v.subject)) v = await Hikari.groupMetadata(id) || {};
        resolve(v.name || v.subject || PhoneNumber('+' + id.replace('@s.whatsapp.net', '')).getNumber('international'));
      });
    } else {
      const v = id === '0@s.whatsapp.net' ? { id, name: 'WhatsApp' } :
                id === Hikari.decodeJid(Hikari.user.id) ? Hikari.user :
                (store.contacts[id] || {});
      return (withoutContact ? '' : v.name) || v.subject || v.verifiedName || PhoneNumber('+' + jid.replace('@s.whatsapp.net', '')).getNumber('international');
    }
  };

  Hikari.public = true;

  Hikari.serializeM = (m) => smsg(Hikari, m, store);

  //~~~~~Memeriksa Koneksi~~~~~//
  Hikari.ev.on('connection.update', (update) => {
    const { connection, lastDisconnect } = update;
    if (connection === 'close') {
      const reason = new Boom(lastDisconnect?.error)?.output.statusCode;
      if ([DisconnectReason.badSession, DisconnectReason.connectionClosed, DisconnectReason.connectionLost, DisconnectReason.connectionReplaced, DisconnectReason.restartRequired, DisconnectReason.timedOut].includes(reason)) {
        StartHikariArchive();
      } else if (reason === DisconnectReason.loggedOut) {
        console.log('Logged out, please reconnect.');
      } else {
        Hikari.end(`Unknown DisconnectReason: ${reason}|${connection}`);
      }
    } else if (connection === 'open') {
      console.log('[Connected] ' + JSON.stringify(Hikari.user.id, null, 2));
    }
  });

  //~~~~~Menyimpan Session~~~~~//
  Hikari.ev.on('creds.update', saveCreds);

  Hikari.sendText = (jid, text, quoted = '', options) => Hikari.sendMessage(jid, { text: text, ...options }, { quoted });

  Hikari.downloadMediaMessage = async (message) => {
    const mime = (message.msg || message).mimetype || '';
    const messageType = message.mtype ? message.mtype.replace(/Message/gi, '') : mime.split('/')[0];
    const stream = await downloadContentFromMessage(message, messageType);
    let buffer = Buffer.from([]);
    for await (const chunk of stream) {
      buffer = Buffer.concat([buffer, chunk]);
    }
    return buffer;
  };

  return Hikari;
}

StartHikariArchive();

function smsg(Hikari, m, store) {
  if (!m) return m;
  const M = proto.WebMessageInfo;
  if (m.key) {
    m.id = m.key.id;
    m.isBaileys = m.id.startsWith('BAE5') && m.id.length === 16;
    m.chat = m.key.remoteJid;
    m.fromMe = m.key.fromMe;
    m.isGroup = m.chat.endsWith('@g.us');
    m.sender = Hikari.decodeJid(m.fromMe && Hikari.user.id || m.participant || m.key.participant || m.chat || '');
    if (m.isGroup) m.participant = Hikari.decodeJid(m.key.participant) || '';
  }
  if (m.message) {
    m.mtype = getContentType(m.message);
    m.msg = (m.mtype == 'viewOnceMessage' ? m.message[m.mtype].message[getContentType(m.message[m.mtype].message)] : m.message[m.mtype]);
    m.body = m.message.conversation || m.msg.caption || m.msg.text || (m.mtype == 'listResponseMessage') && m.msg.singleSelectReply.selectedRowId || (m.mtype == 'buttonsResponseMessage') && m.msg.selectedButtonId || (m.mtype == 'viewOnceMessage') && m.msg.caption || m.text;
    let quoted = m.quoted = m.msg.contextInfo ? m.msg.contextInfo.quotedMessage : null;
    m.mentionedJid = m.msg.contextInfo ? m.msg.contextInfo.mentionedJid : [];
    if (m.quoted) {
      let type = getContentType(quoted);
      m.quoted = m.quoted[type];
      if (['productMessage'].includes(type)) {
        type = getContentType(m.quoted);
        m.quoted = m.quoted[type];
      }
      if (typeof m.quoted === 'string') m.quoted = { text: m.quoted };
      m.quoted.mtype = type;
      m.quoted.id = m.msg.contextInfo.stanzaId;
      m.quoted.chat = m.msg.contextInfo.remoteJid || m.chat;
      m.quoted.isBaileys = m.quoted.id ? m.quoted.id.startsWith('BAE5') && m.quoted.id.length === 16 : false;
      m.quoted.sender = Hikari.decodeJid(m.msg.contextInfo.participant);
      m.quoted.fromMe = m.quoted.sender === Hikari.decodeJid(Hikari.user.id);
      m.quoted.text = m.quoted.text || m.quoted.caption || m.quoted.conversation || m.quoted.contentText || m.quoted.selectedDisplayText || m.quoted.title || '';
      m.quoted.mentionedJid = m.msg.contextInfo ? m.msg.contextInfo.mentionedJid : [];
      m.getQuotedObj = m.getQuotedMessage = async () => {
        if (!m.quoted.id) return false;
        const q = await store.loadMessage(m.chat, m.quoted.id, Hikari);
        return smsg(Hikari, q, store);
      };
      const vM = m.quoted.fakeObj = M.fromObject({
        key: {
          remoteJid: m.quoted.chat,
          fromMe: m.quoted.fromMe,
          id: m.quoted.id
        },
        message: quoted,
        ...(m.isGroup ? { participant: m.quoted.sender } : {})
      });
      m.quoted.delete = () => Hikari.sendMessage(m.quoted.chat, { delete: vM.key });
      m.quoted.copyNForward = (jid, forceForward = false, options = {}) => Hikari.copyNForward(jid, vM, forceForward, options);
      m.quoted.download = () => Hikari.downloadMediaMessage(m.quoted);
    }
  }
  if (m.msg.url) m.download = () => Hikari.downloadMediaMessage(m.msg);
  m.text = m.msg.text || m.msg.caption || m.message.conversation || m.msg.contentText || m.msg.selectedDisplayText || m.msg.title || '';
  m.reply = (text, chatId = m.chat, options = {}) => Buffer.isBuffer(text) ? Hikari.sendMedia(chatId, text, 'file', '', m, { ...options }) : Hikari.sendText(chatId, text, m, { ...options });
  m.copy = () => smsg(Hikari, M.fromObject(M.toObject(m)));
  m.copyNForward = (jid = m.chat, forceForward = false, options = {}) => Hikari.copyNForward(jid, m, forceForward, options);

  return m;
}

